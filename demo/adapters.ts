export class DemoAppAdapter implements AppAdapter {
  async seed(iterRoot: string, config: { seekers: number; employers: number; employees: number }): Promise<SeededEntity[]> {
    const lisaDir = path.join(iterRoot, '.lisa_memory');
    fs.mkdirSync(lisaDir, { recursive: true });

    const dbPath = path.join(lisaDir, 'lisa.db');
    const db = new BetterSqlite3(dbPath);
    applyLisaDbMigrations(db);

    const entities: SeededEntity[] = [];

    for (let i = 1; i <= config.seekers; i++) {
      db.prepare(`INSERT OR IGNORE INTO seeded_entities (entity_type, entity_id, auth_email, auth_password, data)
                  VALUES (?, ?, ?, ?, ?)`).run(
        'demo_user', `user-${i}`, `user${i}@demo.test`, 'demo123',
        JSON.stringify({ name: `Demo User ${i}`, role: 'member' }),
      );
      entities.push({
        alias: `seeker-${i}`,
        id: `user-${i}`,
        type: 'demo_user',
        credentials: { email: `user${i}@demo.test`, password: 'demo123' },
      });
    }

    for (let i = 1; i <= config.employers; i++) {
      db.prepare(`INSERT OR IGNORE INTO seeded_entities (entity_type, entity_id, auth_email, auth_password, data)
                  VALUES (?, ?, ?, ?, ?)`).run(
        'demo_user', `emp-${i}`, `employer${i}@demo.test`, 'demo123',
        JSON.stringify({ name: `Employer ${i}`, role: 'employer' }),
      );
      entities.push({
        alias: `employer-${i}`,
        id: `emp-${i}`,
        type: 'demo_user',
        credentials: { email: `employer${i}@demo.test`, password: 'demo123' },
      });
    }

    db.close();

    fs.writeFileSync(
      path.join(iterRoot, 'mini-sim-export.json'),
      JSON.stringify({ entities, seededAt: new Date().toISOString() }, null, 2),
    );

    // Write seed-data.js so the SPA can bootstrap user list from seeded entities
    const seedUsers = entities.map(e => ({
      id: e.id,
      email: e.credentials!.email,
      password: e.credentials!.password,
      name: `Demo User ${e.id}`,   // overwritten per-entity below
      role: e.alias.startsWith('employer') ? 'employer' : 'member',
    }));
    // Resolve real names from the DB we just wrote
    const nameDb = new BetterSqlite3(dbPath);
    for (const u of seedUsers) {
      const row = nameDb.prepare('SELECT data FROM seeded_entities WHERE entity_id = ?').get(u.id) as any;
      if (row?.data) {
        try {
          u.name = JSON.parse(row.data).name ?? u.name;
        } catch {
          continue;
        }
      }
    }
    nameDb.close();
    const seedDataJs = `// AUTO-GENERATED by DemoAppAdapter.seed() — do not edit manually\nwindow.__DEMO_SEED__ = ${JSON.stringify({ users: seedUsers }, null, 2)};\n`;
    fs.writeFileSync(path.join(__dirname, 'app', 'seed-data.js'), seedDataJs, 'utf8');

    return entities;
  }

  async reset(_iterRoot: string): Promise<void> {}

  async validateEnvironment(): Promise<AppHealthResult> {
    return { healthy: true, errors: [], warnings: [] };
  }

  async verify(iterRoot: string): Promise<void> {
    const exportPath = path.join(iterRoot, 'mini-sim-export.json');
    if (!fs.existsSync(exportPath)) {
      throw new Error(`[demo] verify: mini-sim-export.json not found at ${exportPath}`);
    }
    const { entities } = JSON.parse(fs.readFileSync(exportPath, 'utf8'));
    if (!entities?.length) throw new Error('[demo] verify: no seeded entities — fail closed');
    console.log(`[demo] verify: ${entities.length} entities confirmed`);
  }

  async importRun(_iterRoot: string, _dbUrl: string): Promise<void> {}
}

