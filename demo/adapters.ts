export class DemoSimulationAdapter implements SimulationAdapter {
  async run(iterRoot: string, options: { ticks: number; liveLlm: boolean }): Promise<SimulationRunResult> {
    const simulationId = `demo-sim-${Date.now()}`;
    const dbPath = path.join(iterRoot, '.lisa_memory', 'lisa.db');

    BehaviorEventRecorder.reset();
    const recorder = BehaviorEventRecorder.getInstance(dbPath, 'simulation');
    const now = () => new Date().toISOString();

    // Each discrete agent action gets its own unique execution_id so the recorder's
    // (execution_id, execution_state) idempotency index never silently drops events.
    const ev = (
      agent_id: string, entity_id: string, persona: string, t: number,
      action: string, reasoning: string,
      outcome: RecorderInput['outcome'], outcome_detail: string | null,
      screen_path: string, entity_refs: string | null,
    ): RecorderInput => ({
      simulation_id: simulationId,
      execution_id: randomUUID(),     // unique per action — no idempotency dedup
      agent_id, entity_id, persona_definition_id: persona, tick: t,
      sim_time: now(), action, reasoning,
      event_source: 'agent', event_kind: 'action', execution_state: 'completed',
      outcome, outcome_detail, screen_path, entity_refs,
    });

    // Simulate each tick
    for (let tick = 1; tick <= options.ticks; tick++) {
      // ── seeker-1: login → dashboard → create task → view task ──────────────
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'navigate_to_login', 'Starting session — need to authenticate',
        BEHAVIOR_OUTCOMES.SUCCESS, null, 'auth/login', null));
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'fill_login_credentials', 'Entering email user1@demo.test and password',
        BEHAVIOR_OUTCOMES.SUCCESS, null, 'auth/login',
        JSON.stringify({ user_id: 'user-1' })));
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'submit_login_form', 'Credentials entered — submitting',
        BEHAVIOR_OUTCOMES.SUCCESS, 'Redirected to dashboard', 'auth/login',
        JSON.stringify({ user_id: 'user-1' })));
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'view_task_list', 'On dashboard — scanning existing tasks',
        BEHAVIOR_OUTCOMES.SUCCESS, '3 tasks visible', 'tasks/dashboard', null));

      // Intentional gap: try to create task with empty title
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'attempt_create_task_empty_title', 'Testing whether empty title is validated',
        BEHAVIOR_OUTCOMES.ERROR_422, 'No validation error shown — empty title accepted silently',
        'tasks/dashboard', null));
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'create_task_with_title', 'Creating task with valid title after gap discovery',
        BEHAVIOR_OUTCOMES.SUCCESS, 'Task "Review PR #42" created', 'tasks/dashboard',
        JSON.stringify({ user_id: 'user-1' })));

      // Task detail flow
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'click_task_item', 'Drilling into task-2 to complete it',
        BEHAVIOR_OUTCOMES.SUCCESS, 'Navigated to task/task-2', 'tasks/dashboard',
        JSON.stringify({ task_id: 'task-2' })));
      recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
        'complete_task', 'Marking task-2 as completed',
        BEHAVIOR_OUTCOMES.SUCCESS, 'task-2 status → completed', 'tasks/task-detail',
        JSON.stringify({ task_id: 'task-2', user_id: 'user-1' })));

      // ── seeker-2: login → view dashboard ──────────────────────────────────
      if (tick === 1) {
        recorder.record(ev('seeker-agent-2', 'user-2', 'power_user', tick,
          'navigate_to_login', 'Second seeker starting session',
          BEHAVIOR_OUTCOMES.SUCCESS, null, 'auth/login', null));
        recorder.record(ev('seeker-agent-2', 'user-2', 'power_user', tick,
          'submit_login_form', 'Authenticating as user2@demo.test',
          BEHAVIOR_OUTCOMES.SUCCESS, null, 'auth/login',
          JSON.stringify({ user_id: 'user-2' })));
        recorder.record(ev('seeker-agent-2', 'user-2', 'power_user', tick,
          'view_task_list', 'Checking tasks assigned to me',
          BEHAVIOR_OUTCOMES.SUCCESS, 'Saw task-3 assigned to user-2', 'tasks/dashboard',
          JSON.stringify({ task_id: 'task-3' })));

        // ── employer-1: login → dashboard ───────────────────────────────────
        recorder.record(ev('employer-agent-1', 'emp-1', 'employer_manager', tick,
          'navigate_to_login', 'Employer checking team task board',
          BEHAVIOR_OUTCOMES.SUCCESS, null, 'auth/login', null));
        recorder.record(ev('employer-agent-1', 'emp-1', 'employer_manager', tick,
          'submit_login_form', 'Authenticating as employer1@demo.test',
          BEHAVIOR_OUTCOMES.SUCCESS, null, 'auth/login',
          JSON.stringify({ user_id: 'emp-1' })));
        recorder.record(ev('employer-agent-1', 'emp-1', 'employer_manager', tick,
          'view_dashboard_as_employer', 'Reviewing task distribution across team members',
          BEHAVIOR_OUTCOMES.SUCCESS, '3 tasks visible: 1 completed, 2 pending', 'tasks/dashboard',
          JSON.stringify({ user1_id: 'user-1', user2_id: 'user-2', task1_id: 'task-1', task2_id: 'task-2', task3_id: 'task-3' })));
      }

      // Profile view (tick 2+)
      if (tick === 2) {
        recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
          'navigate_to_profile', 'Checking completion stats',
          BEHAVIOR_OUTCOMES.SUCCESS, null, 'user/profile',
          JSON.stringify({ user_id: 'user-1' })));
        recorder.record(ev('seeker-agent-1', 'user-1', 'casual_member', tick,
          'logout', 'Session complete — signing out',
          BEHAVIOR_OUTCOMES.SUCCESS, null, 'user/profile',
          JSON.stringify({ user_id: 'user-1' })));
      }
    }

    recorder.flush();

    // Query the DB for the actual persisted count — catches any silent idempotency drops
    const countDb = new BetterSqlite3(dbPath);
    const { cnt } = countDb.prepare(
      'SELECT COUNT(*) as cnt FROM behavior_events WHERE simulation_id = ?'
    ).get(simulationId) as { cnt: number };
    countDb.close();

    console.log(`[demo] simulation: ${options.ticks} ticks, ${cnt} behavior events persisted`);
    return { simulationId, ticksCompleted: options.ticks, behaviorEventsWritten: cnt };
  }

  async exportEntities(iterRoot: string, entities: SeededEntity[]): Promise<void> {
    const dbPath = path.join(iterRoot, '.lisa_memory', 'lisa.db');

    // ── Extract candidate flows from behavior events ─────────────────────────
    const db = new BetterSqlite3(dbPath);
    let simulationId = 'demo';
    let flows: Array<{ screen: string; actions: string[]; errorCount: number }> = [];
    try {
      const simRow = db.prepare('SELECT simulation_id FROM behavior_events ORDER BY recorded_at ASC LIMIT 1').get() as any;
      if (simRow) simulationId = simRow.simulation_id;

      // Group events by screen_path
      const rows = db.prepare(`
        SELECT screen_path, action, outcome, COUNT(*) as cnt
        FROM behavior_events
        WHERE screen_path IS NOT NULL
        GROUP BY screen_path, action, outcome
        ORDER BY screen_path, cnt DESC
      `).all() as Array<{ screen_path: string; action: string; outcome: string; cnt: number }>;

      const screenMap = new Map<string, { actions: string[]; errorCount: number }>();
      for (const row of rows) {
        if (!screenMap.has(row.screen_path)) screenMap.set(row.screen_path, { actions: [], errorCount: 0 });
        const entry = screenMap.get(row.screen_path)!;
        entry.actions.push(row.action);
        if (row.outcome.startsWith('error')) entry.errorCount++;
      }
      flows = Array.from(screenMap.entries()).map(([screen, data]) => ({ screen, ...data }));
    } finally {
      db.close();
    }

    console.log(`[demo] exportEntities: extracted ${flows.length} candidate flows from ${simulationId}`);

    // ── Write candidate_flows.yaml ────────────────────────────────────────────
    const candidateFlowsPath = path.join(iterRoot, 'candidate_flows.yaml');
    const yaml = [
      `# candidate_flows.yaml — extracted from simulation ${simulationId}`,
      `# Generated: ${new Date().toISOString()}`,
      `# Entities: ${entities.map(e => e.alias).join(', ')}`,
      ``,
      ...flows.map(f => [
        `- screen: "${f.screen}"`,
        `  actions:`,
        ...f.actions.map(a => `    - "${a}"`),
        `  error_rate: ${f.errorCount > 0 ? (f.errorCount / f.actions.length).toFixed(2) : '0.00'}`,
        `  gap_detected: ${f.errorCount > 0}`,
      ].join('\n')),
    ].join('\n');
    fs.writeFileSync(candidateFlowsPath, yaml, 'utf8');

    // ── Discover data keys (try lisa-mcp, fall back to static map) ───────────
    const discoveredKeys = await this.discoverDataKeys(iterRoot);

    // ── Write generated Playwright specs ─────────────────────────────────────
    const specsDir = path.join(iterRoot, 'generated-flows');
    fs.mkdirSync(specsDir, { recursive: true });

    // 1. login_happy_path.spec.ts — should PASS
    fs.writeFileSync(path.join(specsDir, 'login_happy_path.spec.ts'), generateLoginSpec(discoveredKeys));

    // 2. task_completion_flow.spec.ts — should PASS
    fs.writeFileSync(path.join(specsDir, 'task_completion_flow.spec.ts'), generateTaskCompletionSpec(discoveredKeys));

    // 3. task_creation_validation.spec.ts — intentionally FAILS (gap: no validation message)
    fs.writeFileSync(path.join(specsDir, 'task_creation_validation.spec.ts'), generateTaskValidationSpec(discoveredKeys));

    console.log(`[demo] exportEntities: 3 generated specs written to ${specsDir}`);
    console.log(`[demo] exportEntities: data keys source — ${discoveredKeys.source}`);
    console.log(`[demo]   discovered screens: ${Object.keys(discoveredKeys.keys).join(', ')}`);
    console.log(`[demo]   gap: task_creation_validation.spec.ts will fail — no validation message rendered`);
  }

  async clean(_iterRoot: string): Promise<void> {}

  private async discoverDataKeys(iterRoot: string): Promise<{ source: string; keys: Record<string, string[]> }> {
    try {
      const lisaPackage = '@kaneshir/lisa-mcp';
      const { buildLisaMcpCommand, verifyBinary } = await import(lisaPackage) as LisaMcpModule;
      const verify = await verifyBinary();
      if (!verify.ok) throw new Error(verify.error ?? 'binary not ready');

      const { cmd, args } = buildLisaMcpCommand();
      const discovered = await runLisaMcpExploration(cmd, args, iterRoot);
      if (discovered) {
        return { source: `lisa-mcp ${verify.version}`, keys: discovered };
      }
    } catch (err) {
      console.log(`[demo] lisa-mcp unavailable (${(err as Error).message}) — using static key map`);
    }
    return { source: 'static-key-map', keys: DATA_KEY_MAP };
  }
}

// ---------------------------------------------------------------------------
// Lisa MCP exploration — JSON-RPC 2.0 over stdio
// ---------------------------------------------------------------------------
async function runLisaMcpExploration(
  cmd: string, args: string[], iterRoot: string,
): Promise<Record<string, string[]> | null> {
  const appDir = path.resolve(__dirname, 'app');
  const lisaMemoryDir = path.join(iterRoot, '.lisa_memory');

  return new Promise((resolve) => {
    const timeout = setTimeout(() => { proc.kill(); resolve(null); }, 15_000);
    const proc = spawn(cmd, args, {
      env: { ...process.env, LISA_MEMORY_DIR: lisaMemoryDir },
      stdio: ['pipe', 'pipe', 'pipe'],
    });

    let buf = '';
    const pending = new Map<number, (r: any) => void>();
    let nextId = 1;

    proc.stdout.on('data', (chunk: Buffer) => {
      buf += chunk.toString();
      const lines = buf.split('\n');
      buf = lines.pop() ?? '';
      for (const line of lines) {
        if (!line.trim()) continue;
        try {
          const msg = JSON.parse(line);
          if (msg.id != null && pending.has(msg.id)) {
            pending.get(msg.id)!(msg);
            pending.delete(msg.id);
          }
        } catch {
          continue;
        }
      }
    });

    proc.on('error', () => { clearTimeout(timeout); resolve(null); });
    proc.on('close', () => { clearTimeout(timeout); });

    const send = (obj: object) => {
      proc.stdin.write(JSON.stringify(obj) + '\n');
    };

    const call = (method: string, params: object): Promise<any> => {
      return new Promise((res) => {
        const id = nextId++;
        pending.set(id, res);
        send({ jsonrpc: '2.0', id, method, params });
      });
    };

    const run = async () => {
      try {
        // Initialize
        await call('initialize', {
          protocolVersion: '2024-11-05',
          capabilities: {},
          clientInfo: { name: 'stf-demo', version: '1.0.0' },
        });
        send({ jsonrpc: '2.0', method: 'notifications/initialized', params: {} });

        const discovered: Record<string, string[]> = {};

        // Navigate to each screen and explore
        const screens: Array<{ hash: string; name: string }> = [
          { hash: 'home', name: 'home' },
          { hash: 'login', name: 'login' },
          { hash: 'dashboard', name: 'dashboard' },
          { hash: 'profile', name: 'profile' },
        ];

        for (const screen of screens) {
          const navResult = await call('tools/call', {
            name: 'lisa_navigate',
            arguments: { url: `file://${appDir}/index.html#${screen.hash}` },
          });
          if (navResult?.error) continue;

          const exploreResult = await call('tools/call', {
            name: 'lisa_explore_screen',
            arguments: {},
          });
          const text = exploreResult?.result?.content?.[0]?.text ?? '';
          // Parse data-key values from explore output
          const keyMatches = [...text.matchAll(/data-key[:\s"']+([a-z0-9_.{}-]+)/gi)];
          if (keyMatches.length > 0) {
            discovered[screen.name] = [...new Set(keyMatches.map(m => m[1]))];
          } else {
            discovered[screen.name] = DATA_KEY_MAP[screen.name as keyof typeof DATA_KEY_MAP] ?? [];
          }
        }

        proc.kill();
        clearTimeout(timeout);
        resolve(Object.keys(discovered).length > 0 ? discovered : null);
      } catch {
        proc.kill();
        clearTimeout(timeout);
        resolve(null);
      }
    };

    run();
  });
}

