// ---------------------------------------------------------------------------
// BrowserAdapter — routes on project param
// ---------------------------------------------------------------------------
export class DemoBrowserAdapter implements BrowserAdapter {
  constructor(private readonly demoRoot: string) {}

  async runSpecs(options: { iterRoot: string; project: string; allowFailures: boolean }): Promise<BrowserRunResult> {
    if (options.project === 'generate-flows') {
      // GENERATE_FLOWS phase: specs already written in exportEntities — nothing to do
      console.log('[demo] browser: generate-flows — specs already generated in exportEntities');
      return { passed: 0, failed: 0, total: 0, resultsPath: '' };
    }

    if (options.project === 'generated-flows') {
      return this.runGeneratedFlows(options.iterRoot, options.allowFailures);
    }

    // Default: regression (static navigation.spec.ts)
    return this.runRegression(options.iterRoot, options.allowFailures);
  }

  private runRegression(iterRoot: string, allowFailures: boolean): BrowserRunResult {
    const resultsPath = path.join(iterRoot, 'flow-results.json');
    const configPath = path.join(this.demoRoot, 'playwright.config.ts');
    const appDir = path.join(this.demoRoot, 'app');
    const cmd = `npx playwright test --config=${configPath} --reporter=json`;
    const env = { ...process.env, PLAYWRIGHT_JSON_OUTPUT_NAME: resultsPath, DEMO_APP_DIR: appDir };
    try {
      execSync(cmd, { cwd: this.demoRoot, env, stdio: 'inherit' });
    } catch {
      if (!allowFailures) throw new Error('[demo] Regression Playwright flows failed');
    }
    return { passed: 0, failed: 0, total: 0, resultsPath };
  }

  private runGeneratedFlows(iterRoot: string, allowFailures: boolean): BrowserRunResult {
    const specsDir = path.join(iterRoot, 'generated-flows');
    if (!fs.existsSync(specsDir)) {
      console.log('[demo] browser: no generated-flows dir found — skipping generated flows');
      return { passed: 0, failed: 0, total: 0, resultsPath: '' };
    }
    const resultsPath = path.join(iterRoot, 'generated-flow-results.json');
    const configPath = path.join(this.demoRoot, 'generated-playwright.config.ts');
    const appDir = path.join(this.demoRoot, 'app');
    const cmd = `npx playwright test --config=${configPath} --reporter=json`;
    const existingNodePath = process.env.NODE_PATH ?? '';
    const packageNodeModules = path.resolve(this.demoRoot, '..', 'node_modules');
    const env = {
      ...process.env,
      PLAYWRIGHT_JSON_OUTPUT_NAME: resultsPath,
      GENERATED_FLOWS_DIR: specsDir,
      DEMO_APP_DIR: appDir,
      NODE_PATH: existingNodePath ? `${packageNodeModules}:${existingNodePath}` : packageNodeModules,
    };
    try {
      execSync(cmd, { cwd: this.demoRoot, env, stdio: 'inherit' });
    } catch {
      if (!allowFailures) throw new Error('[demo] Generated Playwright flows failed');
    }
    return { passed: 0, failed: 0, total: 0, resultsPath };
  }
}

// ---------------------------------------------------------------------------
// ScoringAdapter — real dimension computation
// ---------------------------------------------------------------------------
export class DemoScoringAdapter implements ScoringAdapter {
  async score(iterRoot: string): Promise<FabricScore> {
    const regressionResults = this.parseResults(path.join(iterRoot, 'flow-results.json'));
    const generatedResults = this.parseResults(path.join(iterRoot, 'generated-flow-results.json'));
    const coverageDelta = this.computeCoverageDelta(iterRoot);

    const regTotal = regressionResults.total;
    const regPassed = regressionResults.passed;
    const genTotal = generatedResults.total;
    const genPassed = generatedResults.passed;
    const genFailed = generatedResults.failed;

    // discovery_yield: generated flow failures that represent real gaps
    const discoveryYield = genFailed > 0 ? Math.min(10, genFailed * 3) : 0;

    // flow_coverage: combined pass rate across regression AND generated flows
    // Intentional gap-probe failures in generated specs honestly deflate this score,
    // showing that not all user journeys are working correctly.
    const combinedTotal = regTotal + genTotal;
    const combinedPassed = regPassed + genPassed;
    const flowCoverage = combinedTotal > 0 ? Math.round((combinedPassed / combinedTotal) * 10) : 5;

    // regression_health: 10 - penalty per regression failure
    const regressionHealth = regTotal > 0
      ? Math.max(0, 10 - (regressionResults.failed * 2))
      : 10;

    // fixture_health: were entities seeded and verified?
    const exportPath = path.join(iterRoot, 'mini-sim-export.json');
    const fixtureHealth = fs.existsSync(exportPath) ? 10 : 0;

    // persona_realism: based on behavior event diversity
    const dbPath = path.join(iterRoot, '.lisa_memory', 'lisa.db');
    const personaRealism = this.computePersonaRealism(dbPath);

    const overall = Math.round(
      (regressionHealth * 0.30 + flowCoverage * 0.25 + discoveryYield * 0.20 +
       personaRealism * 0.15 + fixtureHealth * 0.10) * 10
    ) / 10;

    const score: FabricScore = {
      simulationId: 'demo',
      generatedAt: new Date().toISOString(),
      overall,
      dimensions: {
        persona_realism: personaRealism,
        coverage_delta: coverageDelta,
        fixture_health: fixtureHealth,
        discovery_yield: discoveryYield,
        regression_health: regressionHealth,
        flow_coverage: flowCoverage,
      },
      details: {
        regression: { total: regTotal, passed: regPassed, failed: regressionResults.failed },
        generated: { total: genTotal, passed: genPassed, failed: genFailed },
      },
    };

    fs.writeFileSync(path.join(iterRoot, 'fabric-score.json'), JSON.stringify(score, null, 2));
    return score;
  }

  private parseResults(filePath: string): { total: number; passed: number; failed: number } {
    if (!fs.existsSync(filePath)) return { total: 0, passed: 0, failed: 0 };
    try {
      const results = JSON.parse(fs.readFileSync(filePath, 'utf8'));
      let passed = 0; let failed = 0;
      const countSpecs = (suites: any[]) => {
        for (const s of suites ?? []) {
          for (const spec of s.specs ?? []) { spec.ok ? passed++ : failed++; }
          countSpecs(s.suites ?? []);
        }
      };
      countSpecs(results.suites ?? []);
      return { total: passed + failed, passed, failed };
    } catch { return { total: 0, passed: 0, failed: 0 }; }
  }

  private computeCoverageDelta(iterRoot: string): number {
    // Derive previous iter root from naming convention (iter-002 → iter-001)
    const iterName = path.basename(iterRoot);
    const match = iterName.match(/^iter-(\d+)$/);
    if (!match) return 0;
    const prevNum = parseInt(match[1], 10) - 1;
    if (prevNum < 1) return 0;
    const prevIterRoot = path.join(path.dirname(iterRoot), `iter-${String(prevNum).padStart(3, '0')}`);
    if (!fs.existsSync(prevIterRoot)) return 0;

    const curr = this.parseResults(path.join(iterRoot, 'flow-results.json'));
    const prev = this.parseResults(path.join(prevIterRoot, 'flow-results.json'));
    if (prev.total === 0) return 0;

    const prevRate = prev.passed / prev.total;
    const currRate = curr.total > 0 ? curr.passed / curr.total : 0;
    return Math.max(0, Math.min(10, Math.round((currRate - prevRate) * 100)));
  }

  private computePersonaRealism(dbPath: string): number {
    if (!fs.existsSync(dbPath)) return 5;
    try {
      const db = new BetterSqlite3(dbPath);
      const row = db.prepare(`
        SELECT
          COUNT(DISTINCT agent_id) as agent_count,
          COUNT(DISTINCT screen_path) as screen_count,
          COUNT(DISTINCT entity_id) as entity_count,
          COUNT(*) as total_events
        FROM behavior_events
      `).get() as any;
      db.close();
      if (!row || row.total_events === 0) return 5;
      // Score based on diversity: more agents, more screens = more realistic
      const agentScore = Math.min(3, row.agent_count);
      const screenScore = Math.min(4, row.screen_count);
      const entityScore = Math.min(3, row.entity_count);
      return agentScore + screenScore + entityScore;
    } catch { return 5; }
  }
}

// ---------------------------------------------------------------------------
// FeedbackAdapter
// ---------------------------------------------------------------------------
export class DemoFeedbackAdapter implements FeedbackAdapter {
  async feedback(iterRoot: string, options: {
    score: FabricScore;
    loopId: string;
    iteration: number;
    previousIterRoot: string | null;
  }): Promise<FabricFeedback> {
    const generatedResultsPath = path.join(iterRoot, 'generated-flow-results.json');
    let failedFlows: Array<import('../dist/index.js').PlaywrightFailedFlow & { suggested_scenario: string | null }> = [];

    if (fs.existsSync(generatedResultsPath)) {
      try {
        const parsed = parsePlaywrightResults(generatedResultsPath);
        failedFlows = parsed.failed_flows.map(f => ({ ...f, suggested_scenario: null }));
      } catch (err) {
        console.log(`[demo] unable to parse generated-flow-results.json (${(err as Error).message})`);
      }
    }

    const personaAdjustments: PersonaAdjustment[] = failedFlows.length > 0
      ? [{
          persona_id: 'casual_member',
          field: 'pressure.urgency',
          old_value: 'low',
          new_value: 'high',
          reason: `${failedFlows.length} gap${failedFlows.length === 1 ? '' : 's'} found in generated flows`,
        }]
      : [];

    const feedback: FabricFeedback = {
      schema_version: 1,
      loop_id: options.loopId,
      iteration: options.iteration,
      simulation_id: options.score.simulationId,
      previous_iteration_root: options.previousIterRoot,
      generated_specs: [],
      score_snapshot: options.score,
      failed_flows: failedFlows,
      persona_adjustments: personaAdjustments,
    };

    fs.writeFileSync(path.join(iterRoot, 'fabric-feedback.json'), JSON.stringify(feedback, null, 2));
    return feedback;
  }
}

